    public void setMaxSpinnerValueproduct(JSpinner spinQtyproduct, JComboBox comboProductID) throws SQLException {
        try {
            Connection con = connect.getcon();
            int productId = Integer.parseInt(comboProductID.getSelectedItem().toString());
            String sql = "SELECT Stock FROM products WHERE ProductId = ?";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.setInt(1, productId);
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                int stock = rs.getInt("Stock");
                SpinnerNumberModel model = new SpinnerNumberModel(1, 1, stock, 1);
                spinQtyproduct.setModel(model);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public void checkStock(JSpinner spinner, String productId) {
        try {
            Connection conn = connect.getcon();
            String sql = "SELECT Stock FROM products WHERE ProductId = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, productId);
            ResultSet rs = pstmt.executeQuery();
            if (rs.next()) {
                int pilih = tableCart.getSelectedRow();
                if (pilih == -1) {
                    return;
                }
                int stock = rs.getInt("Stock");
                int qty = Integer.parseInt(tablemodel2.getValueAt(pilih, 4).toString());
                if ((Integer) spinner.getValue() >= (stock + qty)) {
                    spinner.setValue(stock + qty);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

